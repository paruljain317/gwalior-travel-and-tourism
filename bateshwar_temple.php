		<h4 style="font-family: 'Limelight' ;font-size: 30px; font-weight: "><u>History Of Bateshwar Temple: </u></h4>
		<p style="font-family: cursive;font-size: 18px;" class="text-justify">According to Madhya Pradesh Directorate of Archaeology, this group of 200 temples were built during the reign of Gurjara-Pratihara Dynasty. According to Michael Meister, an art historian and a professor specializing in Indian temple architecture, the earliest temples in the Bateshwar group near Gwalior are likely from the 750-800 CE period.<br><br>

		The temples were destroyed after the 13th century; it is not clear if this was by an earthquake, or Muslim forces. The site was visited and its ruins reported by Alexander Cunningham in 1882 as a "collection of more than 100 temples large and small to the southeast of Paravali Padavali", the latter with a "very fine old temple". Bateshwar was notified by Archaeological Survey of India (ASI) as a protected site in 1920. Limited recovery, standardized temple numbering, ruins isolation with photography, and site conservation effort was initiated during the colonial British era. Several scholars studied the site and included them in their reports. For example, the French archeologist Odette Viennot published a paper in 1968 that included a discussion and photographs of the numbered Batesvar temples.</p><br>

