		<p style="font-family: cursive;font-size: 18px;" class="text-justify">Bateshwar Hindu temples (or Batesara, Bateśvar) are a group of nearly 200 sandstone Hindu temples and their ruins in north Madhya Pradesh in post-Gupta, early Gurjar-Pratihara style of North Indian temple architecture. It is about 35 kilometres (22 mi) north of Gwalior and about 30 kilometres (19 mi) east of Morena town. The temples are mostly small and spread over about 25 acres (10 ha) site. They are dedicated to Shiva, Vishnu and Shakti - representing the three major traditions within Hinduism. The site is within the Chambal River valley ravines, on the north-western slope of a hill near Padavali known for its major medieval era Vishnu temple. The Bateshwar temples were built between the 8th and the 10th-century.[1] The site is likely named after the Bhuteshvar Temple, the largest Shiva temple at the site. It is also referred to as Batesvar temples site or Batesara temples site.<br>
