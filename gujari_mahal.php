		<p style="font-family: cursive;font-size: 18px;" class="text-justify">The Gujari Mahal Archeological Museum or State Archaeological Museum, sometimes called the "Gwalior Fort Museum", is a state museum in Gwalior, located in the fortress of Gujari Mahal. It displays numerous artifacts of the region, including a fragment of the Garuda capital of the Heliodorus pillar from Vidisha.<br>This one is for the history buffs! This massive archeological museum deserves a separate mention. This 15th Century Mahal offers a great insight into the rulers of Gwalior. You will also see a large collection of images of Bagh Cave paintings here.</p><br>

		
		<h4 style="font-family: 'Limelight' ;font-size: 30px; font-weight: "><u>History: </u></h4>
		<p style="font-family: cursive;font-size: 18px;" class="text-justify">The palace of Gujari Mahal was built by Raja Man Singh Tomar for his wife Mrignayani, a Gujar princess in the 15th century. She demanded a separate palace for herself with a regular water supply through an aqueduct from the nearby Rai River. <br>
		In the years that followed, the palace has witnessed many ups and downs. It also changed hands many times and has been held by the Tomars, Mughals, Marathas, and the British, before finally being handed over to the Scindias. Gujari Mahal is now in ruins and the palace has been converted into an archaeological museum. Rare artifacts at the museum include Hindu and Jain sculptures dated to the 1st and 2nd centuries BCE, miniature statue of Salabhanjika, terracotta items and replicas of frescoes seen in the Bagh Caves.</p><br>

