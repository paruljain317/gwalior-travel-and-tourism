		<p style="font-family: cursive;font-size: 18px;" class="text-justify">Referred to as 'the pearl amongst fortresses in India' by Mughal emperor Babur, the Gwalior Fort is one of the most impenetrable fortresses located in the entire northern and southern India and is a place you definitely must visit. Situated on the top of a vast rocky mountain near Gwalior in Madhya Pradesh in central India, this imposing structure dominates the entire city of Gwalior. An inseparable part of the identity and architecture of the city, evidence suggests that it has been around since 6th Century. It is also the places where the second oldest reference of the number 'zero' has been found in the form of a carving inside a temple on the top of the fort.<br><br>
		The construction of the Gwalior Fort took place in two parts, in two different time periods and thus, this architectural marvel has an intriguing history attached to it. It has passed from the possession of one dynasty to the other, multiple times. The entire fort complex is well maintained and includes temples, water tanks and palaces such as Man Mandir, the Gujari, the Jahangir, the Karan, and the Shah Jahan.</p><br>
		<h4 style="font-family: 'Limelight' ;font-size: 30px; font-weight: "><u>Gwalior Fort History: </u></h4>
		<p style="font-family: cursive;font-size: 18px;" class="text-justify">The exact period during which the Gwalior Fort was constructed is not yet known. According to legends, this imposing fort was built in the 3rd century by a local king Suraj Sen. The king had recently been cured of leprosy with the help and blessings of a sage named Gwalipa, who had offered him water from a sacred pond. The grateful king then built this fort and named it after the sage. The word Gwalior is derived from the saint's name- Gwalipa. The sage bestowed the title Pal, which means protector, upon the king; and declared that the fort would remain in his family's possession as long as they bore this title. Interestingly enough, the fort remained with the 83 descendants of Suraj Sen Pal, but the 84th descendant named Tej Karan lost the fort.<br><br>In the years that followed, the Gwalior Fort has witnessed many ups and downs. It also changed hands many times and has been held by the Tomars, Mughals, Marathas, and the British, before finally being handed over to the Scindias.</p><br>

		<h4 style="font-family: 'Limelight' ;font-size: 30px; font-weight: "><u>Architecture of Gwalior Fort:</u></h4>
		<p style="font-family: cursive;font-size: 18px;" class="text-justify">Surrounded by concrete walls of sandstone, the Gwalior Fort encloses three temples, six palaces and several water tanks, and is truly an architectural marvel. The different palaces and temples are a reflection of the architectural finery and skill that existed during those times and continues to be appreciated till date.<br><br>
		The most beautiful place in the entire premises is no doubt the Man Mandir Palace, which with its amazingly elaborate structure, seems to hang at the edge of the striking fort. Blue ceramic tiles form the facade of this breathtakingly beautiful palace. The Teli-ka-Mandir, built in the 9th-century Dravidian style, rises to a height of over 100 feet and is famous for its blend of South Indian architecture with North Indian decorative motifs, as well as an exquisitely sculpted exterior. The Saas-Bahu temples on the eastern side of the fort are also larger than life examples of the 11th-century temple architecture.</p><br>

