		<p style="font-family: cursive;font-size: 18px;" class="text-justify">The Jai Vilas Palace, also known as the Jai Vilas Mahal is situated in Gwalior, Madhya Pradesh and is a symbol of age-old Indian culture and opulence, preserved in the modern day. The Maharaja of Gwalior, Jayaji Rao Scindia, had this magnificent edifice built to ensure a grand welcome for King Edward VII, the then Prince of Wales in 1874. Today, it serves as a residence for the descendants of the royal Maratha Scindia family. The palace also serves as a museum stretched over 35 rooms, houses the Chitrangada Raje Art Gallery and a library that comprises more than 5,000 books. However, it is most popular for its enormous Darbar Hall that is beautified with golden and gilted carvings and tresses. This cavernous room is 100 ft. in height and 50 ft. in width.<br><br>
		Moreover, each room in the regal palace contains curios of historical importance, including a collection of swords that date back to the times of Shah Jahan and Aurangzeb, and the original Dhal (shield) that belonged to freedom fighter Rani Laxmibai. The venture cost a total of INR 1 crore, which is likely to have been quite an extravagant affair in that era. Covering a floor area of about 75 acres, the Jai Vilas Palace has an intricately designed Durbar Hall, which houses some of the world's most colossal chandeliers, gilt furnishings, and a large plush carpet. The centre operates with a vision of creating an international cultural centre for the study of Maratha dynasty.</p><br>
		<h4 style="font-family: 'Limelight' ;font-size: 30px; font-weight: "><u>History of Jai Vilas Palace: </u></h4>
		<p style="font-family: cursive;font-size: 18px;" class="text-justify">It was the Maharaja's wish to bring the Palace of Versailles to Gwalior. So, when the occasion arose for King Edward's arrival in the year 1875, he built the Jai Vilas Palace by putting prisoners from the Gwalior fort to work on its construction. Workers spent 12 years fabricating the plush carpet in the main attraction of the Palace - the Durbar Hall. The carpet is one of the largest in the world.<br><br>ISince setting records seems to be set in stone for this monument, the exquisite chandeliers in the hall are no exception. At that time, experts were doubtful that the roof of the great hall could hold the weight of the two massive chandeliers. However, Sir Michael Filose believed in his approach. In order to prove his point, he set up a wide wooden ramp from the floor to the roof and walked a dozen elephants to the top. The elephants together weighed almost twice that of the chandeliers, and yet the roof held its place. In this way, the ingenious architect simplistically proved the strength of his creation. 140 years later, the roof endures.</p><br>

		<h4 style="font-family: 'Limelight' ;font-size: 30px; font-weight: "><u>Architecture of Jai Vilas Palace:</u></h4>
		<p style="font-family: cursive;font-size: 18px;" class="text-justify">The layout of the Jai Vilas Palace is reflective of the colonial period portraying the strong British influence on India. Designed by the well-known architect Sir Michael Filose (Mukhel Sahib), the magnificent palace is a prime example of European architecture, bringing together myriad experiences the great Sardar had on his tour of the West. The first floor is fashioned in Tuscan style, while the second and third floors are inspired from the Italian-Doric and Corinthian methods respectively. All 400 rooms are furnished with Italian marble flooring, Persian carpets, ornamental accessories and rare antiquities from France, China and Italy.<br><br>
		A unique feature of the Palace is an elongated dining table, on which a silver train with cut glass carriages is rallied around to serve delicacies, after-dinner brandy or cigars to important delegates during get-togethers. The backrests of the chairs at the dining table are emblazoned with the royal crest of Gwalior, indicating the meticulously executed hard work undertaken to beautify the stately home. <br>
		Perhaps the most talked-about embellishment is the pair of custom-made chandeliers that Mukhel Sahib ordered from Vienna. Each chandelier is 40 feet high and weighs 3.5 tonnes, the third largest chandeliers in today's world. The late Maharani Vijayraje Scindia, the wife of Maharaja Jayaji Rao Scindia, seemed to be particularly impressed by the chandeliers as she wrote in her autobiography: "Mukhel Saheb (Sir Michael Filose) Sahib saw to it that most of the other things in his palace matched the durbar hall in scale and splendour, and nowhere else is his propensity for the grandiloquent more in evidence than in the crystal chandeliers he ordered for the durbar hall from Vienna. The two main ones which are very nearly a pair are said to be the largest ever made, with the possible exception of the one which hangs in the Czar's winter palace outside Moscow."</p><br>

