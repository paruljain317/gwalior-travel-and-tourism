		<p style="font-family: cursive;font-size: 18px;" class="text-justify">Sasbahu Temple, also called the Sahastrabahu Temple or Harisadanam temple, is an 11th-century twin temple in Gwalior, Madhya Pradesh, India. It was built in 1093 by King Mahipala of the Kachchhapaghata dynasty, according to an inscription found in the larger of the twin temple. It is mostly in ruins and was badly damaged from numerous invasions and Hindu-Muslim wars in the region.<br><br>
		The Saas Bahu temple attracts tourists and devotees alike. Contrary to what the name suggests, the Saas Bahu Temple does not mean Saas( mother-in-law) and bahu(daughter-in-law) but is rather a short form of Shastra Bahu, another name of Lord Vishnu. These are two temples situated adjacent to each other and are decorated with impeccable carvings and sculptures.</p><br>

