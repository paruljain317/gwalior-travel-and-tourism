		<p style="font-family: cursive;font-size: 18px;" class="text-justify">The temple's tower and sanctum has been destroyed, but its architecture and damaged carvings can still be appreciated from the ruins. The jagati platform is 100 feet (30 m) long and 63 feet (19 m) wide, on a square plan.The temple was three-storeyed, which was one of its distinguishing features and sophistication. It followed a central cluster concept, states Adam Hardy.This temple mainly has three entrances from three different directions. In the fourth direction, there is a room which is currently closed. The entire temple is covered with carvings, notably 4 idols of Brahma, Vishnu and Saraswati above its entrance door. The pillar carvings show Vaishnavism, Shaivism and Shaktism related carvings. The larger temple ornamentation covers all the exterior walls and all surviving interior surfaces.</p><br>

