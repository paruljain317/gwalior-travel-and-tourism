		<p style="font-family: cursive;font-size: 18px;" class="text-justify">Teli ka Mandir, also known as Telika Temple, is a Hindu temple located within the Gwalior Fort in Madhya Pradesh, India. Dedicated to Vishnu, Shiva and Matrikas, it has been variously dated between the early 8th and early 9th century CE.It is an unusual Hindu temple,as it has a rectangular sanctum instead of the typical square. It integrates the architectural elements of the Nagara style and the Valabhi prasada that looks like the Dravidian wagon-vault topped gopuram superstructure. The temple is based on a Pratihara-Gopagiri style North Indian architecture.<br><br>The temple is a classic example of a design based on "musical harmonics" in architecture, one that Hermann Goetz called as a masterpiece of late Gupta era Indian art. It is a 100 feet high temple, which has influence from both Buddhist and Hindu architectural styles. No pillared pavilions and a Buddhist barrel-vaulted roof on top of a Hindu mandir represent the architectural style of Hindus. Apart from this, Chitya type of hall and torana decorations at the entrance gate are influenced from Buddhist style of architecture.</p><br>


		<h4 style="font-family: 'Limelight' ;font-size: 30px; font-weight: "><u>Architecture of Teli Ka Mnadir:</u></h4>
		<p style="font-family: cursive;font-size: 18px;" class="text-justify">The temple has a rectangular triratha sanctum plan that sits on a jagati platform that is a square of 60 feet (18 m). It has a large kapili projecting portico of about 11 feet (3.4 m) towards the east. The tower rises about the rectangular sanctum to a height of 80 feet (24 m). Above it is a barrel vault shaped cap of 30 feet (9.1 m), its length perpendicular to that of the sanctum, that reminds one of South Indian gopuram. It likely had amalaka, kalasha and other ornaments on top, but these are all lost to history. The walls of temple has numerous niches for statues, but it is all empty now and show signs of damage. The niches are topped by tall pediments. The outer dimensions of the sanctum are 60x40 feet with an 80 feet tower above. The doorway into the temple is 35 feet (11 m) high, and it is ornate.The entrance door has a torana or archway accompanied by beautifully decorated sculpted images. These include images of romantic couples, river goddesses, flower decoration and a Garuda. The structure of Garuda is the highest monument in the fort, which can be seen from a distance.</p><br>

