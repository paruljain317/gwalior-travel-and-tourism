		Tigra Dam (also spelled "Tig Dam") creates a freshwater reservoir on the Sank River, about 23 km from Gwalior, Madhya Pradesh. It plays a crucial role in supplying water to the city. The dam is 24 metres high at its crest, and 1341 m long. The reservoir has a capacity of 4.8 million cubic metres and the spillway structure can pass up to 1274 cubic metres per second. It is the primary source of drinking water to the city of Gwalior and also one of the popular picnic places to visit near Gwalior.<br><br>As tourism gained popularity, the tourist department started a boating club here. One can enjoy speed boating, jalpari boat, paddle boating and water scooty rides at Tighra Dam.  Apart from adventure, it also offers serenity with the picturesque view that surrounds the dam. After the construction of the dam, the area became a suitable habitat for several birds and this area was later declared as a Bird Sanctuary. The reservoir is home to many crocodiles and many different fishes can be seen during boating.
