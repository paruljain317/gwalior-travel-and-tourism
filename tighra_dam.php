		<p style="font-family: cursive;font-size: 18px;" class="text-justify">Tigra Dam has been constructed on Sank River in 1916. This dam has been constructed in the vicinity of eleven villages. The villagers depend on this dam for their irrigation, drinking and domestic purpose. Moreover, the dam also provides the water required for the drinking purpose in the Gwalior City. One of the negative and gadha impacts of Tigra dam is that it is constructed in Ghatigora Goan. After the construction of the dam the area became a suitable habitat for several birds and this area was later declared as a Bird Sanctuary. However this also led to the relocation of the people that was residing in the surrounding area of the dam. Despite the positive attributes like control of stream regime, prevention of flood, water for irrigation, dams still present negative impacts on the environment. Some of the negative impacts that can be mentioned are water logging, land loss, relocation of people and silt formation.  The dam was failed twice due to infiltration into its sandstone foundations in 1917 & in 1970 respectively. However, today it has emerged as a favourite destination for families to spend some good time together.<br>
